    private void votar() {
        Voto v = new Voto();
        try {
            v.setCpf(txt_cpf.getText());
            v.setNum(txt_numero.getText());
            if (txt_numero.getText().equals("77")) {
                listaVotos1.add(v);
                a1.salvarArquivo(listaVotos1, "1");
            } else if (txt_numero.getText().equals("51")) {
                listaVotos2.add(v);
                a2.salvarArquivo(listaVotos2, "2");
            } else if (txt_numero.getText().equals("43")) {
                listaVotos3.add(v);
                a3.salvarArquivo(listaVotos3, "3");
            }

            JOptionPane.showMessageDialog(null, "voto realizado com sucesso!");
        } catch (CamposVaziosException e) {
            System.out.println(e.getMessage());
        } catch (CpfInvalidoException e1) {
            System.out.println(e1.getMessage());
        } catch (NumeroErradoException e2) {
            System.out.println(e2.getMessage());
        } finally {
            limparCampos();
            btn_okay.setVisible(false);
        }

    }

