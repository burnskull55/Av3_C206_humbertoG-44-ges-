    private void fazVotacao() {
        //listas aux para contar os votos pelo arquivo
        ArrayList<String> cpfs1 = new ArrayList();
        ArrayList<String> cpfs2 = new ArrayList();
        ArrayList<String> cpfs3 = new ArrayList();
        //percorre os 3 arquivos e preenche as listas
        for (Voto i : a1.ler("1")) {
            cpfs1.add(i.getCpf());
        }
        for (Voto i : a2.ler("2")) {
            cpfs2.add(i.getCpf());
        }
        for (Voto i : a3.ler("3")) {
            cpfs3.add(i.getCpf());
        }
        //mostra as lbl de voto
        lbl_Static_votos.setVisible(true);
        lbl_Static_votos1.setVisible(true);
        lbl_Static_votos2.setVisible(true);
        //mostra quantos votos cada um recebeu 
        lbl_cont1.setText(cpfs1.size() + "");
        lbl_cont2.setText(cpfs2.size() + "");
        lbl_cont3.setText(cpfs3.size() + "");
        //variavel aux para fazer a comparação 
        int aux1, aux2, aux3;
        aux1 = cpfs1.size();
        aux2 = cpfs2.size();
        aux3 = cpfs3.size();
        //menu de comparações

        if (aux1 > aux2 && aux1 > aux3) {
            txta_anuncio.setText("Com " + aux1 + " votos \n"
                    + "     O vencedor é : Paulinho Anão !");

        
        } else if (aux2 > aux1 && aux2 > aux3) {
            txta_anuncio.setText("Com " + aux2 + " votos \n"
                    + "     O vencedor é : Rogerão !");

           

        } else if (aux3 > aux2 && aux3 > aux1) {
            txta_anuncio.setText("Com " + aux3 + " votos \n"
                    + "     O vencedor é : Darth Verde !");

           
        } else if (aux1 == aux2 && aux1 > aux3) {
            fazerSegundoTurno(c1, c2);
        } else if (aux1 == aux3 && aux1 > aux2) {
            fazerSegundoTurno(c2, c3);
        } else if (aux3 == aux2 && aux2 > aux1) {
            fazerSegundoTurno(c2, c1);
        }
        
    }

    private void fazerSegundoTurno(Candidato a, Candidato b) {
        btn_okay.setVisible(true);
        JOptionPane.showMessageDialog(null, "Precisamos de um voto de desempate!");
        txta_anuncio.setText("     Essa não ocorreu um empate! \n"
                + "       " + a.getNome() + " VS " + b.getNome());

    }

   
      
